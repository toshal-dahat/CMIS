<script lang="ts">
  import { getTeamFeedback, getSynthesizedFeedback } from "./competition-api";

  export let competitionId: string;
  export let teamId: string;
  export let feedbackReleaseDate: string | null | undefined;

  let narrative = "";
  let loading = true;
  let isReleased = false;
  let error = "";

  // Watch for change in props to trigger reload
  $: {
    if (competitionId && teamId) {
      void loadData();
    }
  }

  // Reactively check if the feedback should be visible
  $: {
    if (feedbackReleaseDate) {
      const releaseTime = new Date(feedbackReleaseDate).getTime();
      const now = new Date().getTime();
      isReleased = now >= releaseTime;
    } else {
      isReleased = false;
    }
  }

  async function loadData() {
    try {
      loading = true;
      error = "";

      // 1. Fetch feedback metadata (always available to know count/release status)
      const data = await getTeamFeedback(competitionId, teamId);
      isReleased = data.released;

      // 2. Fetch narrative ONLY if released
      if (isReleased) {
        try {
          const synthesis = await getSynthesizedFeedback(competitionId, teamId);
          narrative = synthesis.narrative || "";
        } catch (synthesisError) {
          narrative = "";
          error = "Your constructive narrative is currently being prepared. Check back shortly!";
        }
      }
    } catch (err: any) {
      if (isReleased) {
        error = "Unable to reach the synthesis service.";
      }
    } finally {
      loading = false;
    }
  }

  function formatDate(d: string) {
    return new Date(d).toLocaleDateString('en-US', {
      weekday: 'long', month: 'long', day: 'numeric', year: 'numeric',
      hour: '2-digit', minute: '2-digit'
    });
  }
</script>

<div class="feedback-card" class:locked={!isReleased}>
  <div class="card-header">
    <div class="badge">
      <span class="sparkle">✨</span> Feedback Synthesis
    </div>
  </div>

  {#if loading}
    <div class="status-box">
      <div class="shimmer-line title"></div>
      <div class="shimmer-line body"></div>
      <div class="shimmer-line body"></div>
      <p>Consulting our AI Mentor...</p>
    </div>
  {:else if !isReleased}
    <div class="locked-content">
      <div class="lock-visual">
        <div class="lock-circle">🔒</div>
      </div>
      <h3>Narrative Locked</h3>
      <p>We're aggregating judge perspectives to create your cohesive feedback story.</p>
      <div class="release-info">
        <span class="label">Releasing on</span>
        <span class="date">{feedbackReleaseDate ? formatDate(feedbackReleaseDate) : 'TBD'}</span>
      </div>
    </div>
  {:else if narrative}
    <div class="unlocked-content scale-in">
      <blockquote>
        <span class="quote-mark">“</span>
        <p>{narrative}</p>
      </blockquote>
      <div class="card-footer">
        Generated by CMIS AI synthesis agent based on all judge submissions.
      </div>
    </div>
  {:else}
    <div class="error-content">
      <p>{error || "No narrative found for this team yet."}</p>
    </div>
  {/if}
</div>

